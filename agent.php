    <script>
        // Function to handle form submission with success/error response
        function validateForm(event) {
            event.preventDefault(); // Prevent default form submission
            const form = document.querySelector('form');
            const formData = new FormData(form); // Collect form data

            fetch("https://api.web3forms.com/submit", {
                method: "POST",
                body: formData
            })
            .then(response => response.json())
            .then(data => {
                if (data.success) {
                    showPopup("Thank you for submitting your application! We'll be in touch soon.");
                    form.reset(); // Reset form after submission
                } else {
                    alert("There was an error submitting the form. Please try again.");
                }
            })
            .catch(error => {
                console.error("Error:", error);
                alert("There was an error submitting the form.");
            });
        }

        // Function to display success message in a larger popup and redirect
        function showPopup(message) {
            const popup = document.createElement("div");
            popup.style.position = "fixed";
            popup.style.top = "20%";
            popup.style.left = "50%";
            popup.style.transform = "translate(-50%, -20%)";
            popup.style.backgroundColor = "#4CAF50";
            popup.style.color = "#fff";
            popup.style.padding = "30px 40px"; // Increased padding for a larger popup
            popup.style.fontSize = "1.2rem"; // Larger font size for readability
            popup.style.borderRadius = "8px";
            popup.style.width = "80%"; // Optional: specify width for larger popup on smaller screens
            popup.style.maxWidth = "400px"; // Set a max width for desktop
            popup.style.boxShadow = "0 6px 12px rgba(0,0,0,0.2)";
            popup.style.zIndex = "1000";
            popup.style.textAlign = "center";
            popup.innerText = message;

            document.body.appendChild(popup);

            // Remove the popup after 3 seconds and redirect
            setTimeout(() => {
                popup.remove();
                window.location.href = "index.html";
            }, 3000);
        }
    </script>

